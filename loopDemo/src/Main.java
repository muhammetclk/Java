public class Main {

    public static void main(String[] args) {

        //for

        for(int i=0;i<=10;i++){

            System.out.println("i:"+i);
        }
        System.out.println("for dongusu bitti");

        //while
        int i=1;
        while (i<=10){
            System.out.println("i:"+i);
            i+=2;
        }
        System.out.println("While dongusu bitti");
    }
}
