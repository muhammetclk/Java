public class Main {

    public static void main(String[] args) {

        //for

        for(int i=0;i<=10;i++){

            System.out.println("i:"+i);
        }
        System.out.println("for dongusu bitti");

        //while
        int i=1;
        while (i<=10){
            System.out.println("i:"+i);
            i+=2;
        }
        System.out.println("While dongusu bitti");

        //do-while
        int a=2;
        do{
            System.out.println("a:"+a);
            a+=2;
        }while (a<=10);
        System.out.println("do-While dongusu bitti");

    }
}
